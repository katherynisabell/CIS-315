// A Java program to find the area of a circle

public class CircleArea {
    public static void main(String args[]) {
        // Set up the variables
        
        double radius;
        double area; 
        radius = 20;
        
        // Calculation
        
        area = 3.14159 * radius;
        
        // Display the Result

      System.out.println("The area of a circle with a radius "+ radius + " is " + area);
    }
}